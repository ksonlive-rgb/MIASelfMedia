// 维度分数结构
export interface DimensionScores {
  focus: number;
  emotion: number;
  social: number;
  development: number;
  positive: number;
  family: number;
  parenting: number;
}

// 计算教育画像类型
export function calculateEduType(dimScores: DimensionScores): number {
  const childTotal = dimScores.focus + dimScores.emotion + dimScores.social + dimScores.development + dimScores.positive;
  const envTotal = dimScores.family + dimScores.parenting;

  if (dimScores.development >= 8 && (dimScores.emotion >= 10 || dimScores.social >= 7) && childTotal >= 35) return 7;
  if (envTotal >= 12 && childTotal < 28) return 5;
  if (childTotal >= 28 || envTotal >= 10) return 6;

  if (dimScores.focus <= 4 && dimScores.emotion <= 4 && dimScores.positive <= 3 && envTotal <= 6) return 0;
  if (dimScores.emotion >= 6 && dimScores.focus <= 4 && dimScores.positive <= 4) return 1;
  if (dimScores.focus >= 6 && dimScores.emotion <= 4) return 2;
  if (dimScores.social >= 5 && dimScores.emotion <= 4) return 3;
  if (dimScores.positive <= 2 && envTotal <= 5) return 4;

  return 0;
}

// 处理测试结果
export function processQuizResults(answers: number[], questions: QuizQuestion[]): {
  dimScores: DimensionScores;
  eduType: number;
} {
  const dimScores: DimensionScores = {
    focus: 0,
    emotion: 0,
    social: 0,
    development: 0,
    positive: 0,
    family: 0,
    parenting: 0
  };

  questions.forEach((q, idx) => {
    const answerIdx = answers[idx];
    if (answerIdx !== undefined && answerIdx !== null && q.options[answerIdx]) {
      dimScores[q.dimension] += q.options[answerIdx].score;
    }
  });

  const eduType = calculateEduType(dimScores);

  return { dimScores, eduType };
}

// 获取某年龄组的测试题目（从该年龄题库+通用题中随机抽取30题）
export function getQuestionsForAge(age: AgeGroup): QuizQuestion[] {
  const ageQuestions = questionBank[age] || [];
  const commonQuestions = questionBank["common"] || [];

  const combined = [...ageQuestions, ...commonQuestions];

  // 随机打乱
  for (let i = combined.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [combined[i], combined[j]] = [combined[j], combined[i]];
  }

  return combined.slice(0, 30);
}
